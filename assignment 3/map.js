// Q1:**Map Transformation:**
// Given an array of integers, use the `map` method to square each element and
// return a new array with the squared values.

const originalArray = [1, 2, 3, 4, 5];
const squaredArray = originalArray.map(a => a * a);

console.log(squaredArray); // Output: [1, 4, 9, 16, 25]
